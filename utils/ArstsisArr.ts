// Function to fetch and flatten all albums and EPs
export const fetchAllAlbumsAndEPs = (artists) => {
    const songsArray = [];

    artists.forEach(artist => {
      const { eps, albums } = artist.songs;

      // Flatten EPs into the array
      eps.forEach(ep => {
        songsArray.push({
          title: ep?.title,
          artist: artist?.name,
          cover_image: ep?.cover_image,
          description: `${ep?.title} EP by ${artist?.name}, released on ${ep?.release_date}`,
          songs: ep.tracks
        });
      });

      // Flatten albums into the array
      albums.forEach(album => {
        songsArray.push({
          title: album?.title,
          artist: artist?.name,
          cover_image: album?.cover_image,
          description: `${album?.title} album by ${artist?.name}, released on ${album?.release_date}`,
          songs: albums?.tracks
        });
      });
    });
    // Shuffle the array to display songs randomly
    return songsArray.sort(() => Math.random() - 0.5);
  };

  // Function to fetch tracks by album, EP, or single title
export const fetchTracksByTitle = (title) => {
    let result = null;

    // Loop through each artist in the array
    artistsArr.forEach(artist => {
      const { singles, eps, albums } = artist.songs;

      // Check singles for matching title
      singles.forEach(single => {
        if (single.title.toLowerCase() === title.toLowerCase()) {
          result = {
            title: single.title,
            cover_image: single.cover_image,
            release_date: single.release_date,
            type: 'single',
            tracks: single.tracks
          };
        }
      });

      // Check EPs for matching title
      eps.forEach(ep => {
        if (ep.title.toLowerCase() === title.toLowerCase()) {
          result = {
            title: ep.title,
            cover_image: ep.cover_image,
            release_date: ep.release_date,
            type: 'ep',
            tracks: ep.tracks
          };
        }
      });

      // Check albums for matching title
      albums.forEach(album => {
        if (album.title.toLowerCase() === title.toLowerCase()) {
          result = {
            title: album.title,
            cover_image: album.cover_image,
            release_date: album.release_date,
            type: 'album',
            tracks: album.tracks
          };
        }
      });
    });

    // Return the result or null if no match was found
    return result;
  };





export function formatNumber(num: string) {
    const numValue = parseInt(num);
    if (numValue >= 1000000) {
        return (numValue / 1000000).toFixed(1) + 'M';
    } else if (numValue >= 1000) {
        return (numValue / 1000).toFixed(1) + 'K';
    } else {
        return numValue.toString();
    }
}

